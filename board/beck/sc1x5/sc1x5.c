static void board_init_mac(void)
{
	u8 mac[ETH_ALEN];

	imx_get_mac_from_fuse(0, mac);
	if (!is_valid_ether_addr(mac)) {
		printf("No valid MAC address in fuses\n");
		return;
	}
	printf("MAC address from fuses: %pM\n", mac);
	if (!getenv("ethaddr"))
		eth_setenv_enetaddr("ethaddr", mac);

	imx_get_mac_from_fuse(1, mac);
	if (!is_valid_ether_addr(mac)) {
		printf("No valid MAC for eth1 in fuses\n");
		return;
	}
	eth_setenv_enetaddr("eth1addr", mac);
}

int board_eth_init(bd_t *bis)
{
	int ret;
	int phyaddr;
	
	setup_iomux_fec(CONFIG_FEC_ENET_DEV);

	board_init_mac();

	phyaddr = getenv_ulong("enet_phy_addr", 10, CONFIG_FEC_MXC_PHYADDR);
    
	ret = fecmxc_initialize_multi(bis, CONFIG_FEC_ENET_DEV,	phyaddr, IMX_FEC_BASE);
	if (ret)
		printf("FEC%d MXC: %s:failed\n", CONFIG_FEC_ENET_DEV, __func__);

	return 0;
}

static int setup_fec(int fec_id)
{
	struct iomuxc_gpr_base_regs *const iomuxc_gpr_regs
		= (struct iomuxc_gpr_base_regs *) IOMUXC_GPR_BASE_ADDR;
	int ret;

    /* 
     * Setup clocks for ENET1 and ENET2, because of RESETOUT#
     * issue, see comment at pads above.
     */

	/* Use 50M anatop loopback REF_CLK1 for ENET1, clear gpr1[13], set gpr1[17]*/
	clrsetbits_le32(&iomuxc_gpr_regs->gpr[1], IOMUX_GPR1_FEC1_MASK,
		IOMUX_GPR1_FEC1_CLOCK_MUX1_SEL_MASK);
	/* Use 50M anatop loopback REF_CLK2 for ENET2, clear gpr1[14], set gpr1[18]*/
	clrsetbits_le32(&iomuxc_gpr_regs->gpr[1], IOMUX_GPR1_FEC2_MASK,
		IOMUX_GPR1_FEC2_CLOCK_MUX1_SEL_MASK);

    ret = enable_fec_anatop_clock(0, ENET_50MHZ);		
    ret |= enable_fec_anatop_clock(1, ENET_50MHZ);
	if (ret)
		return ret;

	enable_enet_clk(1);

	return 0;
}

int board_phy_config(struct phy_device *phydev)
{
	unsigned int phyId1, phyId2;

	phyId1 = phy_read(phydev, MDIO_DEVAD_NONE, 0x02);
	phyId2 = phy_read(phydev, MDIO_DEVAD_NONE, 0x03);

	// Micrel/Microchip PHY KSZ8061?
	if ((phyId1 == 0x0022) && ((phyId2 & 0xFFF0) == 0x1570))
	{
		// Workaround for link-up failure, see Silicon Errata
		phy_write(phydev, MDIO_DEVAD_NONE, 0x0D, 0x0001);
		phy_write(phydev, MDIO_DEVAD_NONE, 0x0E, 0x0002);
		phy_write(phydev, MDIO_DEVAD_NONE, 0x0D, 0x4001);
		phy_write(phydev, MDIO_DEVAD_NONE, 0x0E, 0xB61A);
	}

	if (phydev->drv->config)
		phydev->drv->config(phydev);

	return 0;
}
