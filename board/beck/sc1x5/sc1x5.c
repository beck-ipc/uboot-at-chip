#define RESETOUT_GPIO IMX_GPIO_NR(5, 9)

iomux_v3_cfg_t const resetout_pad = {
  MX6_PAD_SNVS_TAMPER9__GPIO5_IO09 | MUX_PAD_CTRL(NO_PAD_CTRL)
};

static void setup_resetout(void)
{
	imx_iomux_v3_setup_pad(resetout_pad);

    /* RESETOUT# is inverted, set to 1 for low, 0 for high */
	gpio_direction_output(RESETOUT_GPIO, 0);
	__udelay(20000);
	gpio_direction_output(RESETOUT_GPIO, 1);
	__udelay(20000);
	gpio_direction_output(RESETOUT_GPIO, 0);	
}

